
public class Mentores extends LiderMovimento {

	
	private String duvidaParaOrganizadores;
	private String ministrarEvento;
	private String compartilharExp;
	private String duvidasAlunos;
	private String solucao;
	
	
	public String getDuvidaParaOrganizadores() {
		return duvidaParaOrganizadores;
	}
	public void setDuvidaParaOrganizadores(String duvidaParaOrganizadores) {
		this.duvidaParaOrganizadores = duvidaParaOrganizadores;
	}
	public String getMinistrarEvento() {
		return ministrarEvento;
	}
	public void setMinistrarEvento(String ministrarEvento) {
		this.ministrarEvento = ministrarEvento;
	}
	public String getCompartilharExp() {
		return compartilharExp;
	}
	public void setCompartilharExp(String compartilharExp) {
		this.compartilharExp = compartilharExp;
	}
	public String getDuvidasAlunos() {
		return duvidasAlunos;
	}
	public void setDuvidasAlunos(String duvidasAlunos) {
		this.duvidasAlunos = duvidasAlunos;
	}
	public String getSolucao() {
		return solucao;
	}
	public void setSolucao(String solucao) {
		this.solucao = solucao;
	}
	
	
	

	
}
