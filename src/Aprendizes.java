
public class Aprendizes extends Aluno {
	
	private String ParticiparEvento;
	private String compartilharResultados;
	private String compartilharExp;
	private String duvidas;
	
	
	public String getParticiparEvento() {
		return ParticiparEvento;
	}
	public void setParticiparEvento(String participarEvento) {
		ParticiparEvento = participarEvento;
	}
	public String getCompartilharResultados() {
		return compartilharResultados;
	}
	public void setCompartilharResultados(String compartilharResultados) {
		this.compartilharResultados = compartilharResultados;
	}
	public String getCompartilharExp() {
		return compartilharExp;
	}
	public void setCompartilharExp(String compartilharExp) {
		this.compartilharExp = compartilharExp;
	}
	public String getDuvidas() {
		return duvidas;
	}
	public void setDuvidas(String duvidas) {
		this.duvidas = duvidas;
	}
	
	
	
}
